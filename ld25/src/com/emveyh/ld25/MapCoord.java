package com.emveyh.ld25;

public class MapCoord {

	private int x;
	private int y;
	
	public MapCoord(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public MapCoord() {
		
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
	
}
